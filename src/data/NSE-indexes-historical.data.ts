const convertShorthand = (value: string) => {
  //  Parse the number part and the suffix
  let number = parseFloat(value.slice(0, -1));
  let suffix = value.slice(-1).toUpperCase();

  //  Determine the multiplier based on the suffix
  let multiplier;
  switch (suffix) {
    case 'K':
      multiplier = 1e3;
      break;
    case 'M':
      multiplier = 1e6;
      break;
    case 'B':
      multiplier = 1e9;
      break;
    default:
      multiplier = 1;
  }

  //  Return the full number
  return number * multiplier;
};

